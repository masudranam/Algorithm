const int M = 1e9 + 7;
const int N = 2e5 + 10;

void solve(){
    int n; cin >> n;
    string t = "aeiou";
    int baki = n % 5;

    string res;
    for(int i = 0; i < 5; i++){
        res += string(n / 5, t[i]); 
        if(baki > 0)res += t[i];
        baki--;
    }
    cout << res << '\n';
}

signed main() {
   ios_base::sync_with_stdio (0);
   cin.tie (0);

   int t = 1;   cin >> t;
   for (int tc = 1; tc <= t; tc++) {
      //cout<<"Case "<<tc<<": ";
      solve();
   }
   return 0;
}
